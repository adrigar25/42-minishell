#include "../minishell.h"

int	ft_echo(char **args)
{
	int	i;
	int	newline;

	printf("Executing builtin: echo\n");
	if (!args || !args[0])
		return (1);
	newline = 1;
	i = 1;
	if (args[i] && ft_strcmp(args[i], "-n") == 0)
	{
		newline = 0;
		i++;
	}
	while (args[i])
	{
		printf("%s", args[i]);
		if (args[i + 1])
			printf(" ");
		i++;
	}
	if (newline)
		printf("\n");
	return (0);
}

#include "../minishell.h"

void	ft_skip_spaces(const char *str, int *i)
{
	if (!str || !i)
		return ;
	while (str[*i] && (str[*i] == ' ' || str[*i] == '\t' || str[*i] == '\n'
			|| str[*i] == '\r' || str[*i] == '\f' || str[*i] == '\v'))
	{
		(*i)++;
	}
}

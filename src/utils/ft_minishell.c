#include <stdio.h>
#include <stdlib.h>
#include <readline/readline.h>
#include "../minishell.h"

int	ft_minishell(void)
{
	char *input;
	while (1)
	{
		input = readline("minishell> ");
		if (!input)
			break ;
			
		free(input);
	}
	
	return (0);
}